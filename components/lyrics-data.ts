// Section break indices (where a new chorus begins)
const SECTION_BREAK_INDICES = new Set([72, 143]);

// Full lyrics text for Pretext (with verse breaks)
export const LYRICS_TEXT = TIMED_WORDS.reduce((acc, w, i) => {
  if (i === 0) return w.word;
  const sep = SECTION_BREAK_INDICES.has(i) ? "\n\n" : " ";
  return acc + sep + w.word;
}, "");

// Precompute: character start index for each word in LYRICS_TEXT
export const WORD_CHAR_STARTS: number[] = [];
{
  let pos = 0;
  for (let i = 0; i < TIMED_WORDS.length; i++) {
    WORD_CHAR_STARTS.push(pos);
    pos += TIMED_WORDS[i].word.length;
    if (i < TIMED_WORDS.length - 1) {
      pos += SECTION_BREAK_INDICES.has(i + 1) ? 2 : 1; // \n\n or space
    }
  }
}

// Helper: get the index of the word currently being sung
export function getCurrentWordIndex(time: number): number {
  for (let i = TIMED_WORDS.length - 1; i >= 0; i--) {
    if (time >= TIMED_WORDS[i].start) return i;
  }
  return -1;
}
